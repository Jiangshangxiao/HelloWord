public class BitOperator {
    public static void main(String[] args) {
        //推导
        //1。先得到2的补码   00000000 00000000 00000000 00000010
        //2. 再得到3的补码   00000000 00000000 00000000 00000011
        //3. 2&3操作        00000000 00000000 00000000 0000010
        //4.运算后的原码      00000000 00000000 00000000 0000010 =》2
        System.out.println(2&3); //2

        //推导
        //1.先得到2的补码  00000000 00000000 00000000 00000010
        //2.~2操作 11111111 11111111 11111111 11111101
        //运算后的原码 10000000 00000000 00000000 00000011 =》-3
        System.out.println(~2); //-3

        //推导
        //1.先得到2的补码 00000000 00000000 00000000 00000010
        //2.再得到3的补码 00000000 00000000 00000000 00000011
        //3.2|3操作 00000000 00000000 00000000 00000011
        //4.运算后的原码 00000000 00000000 00000000 00000011 =》3
        System.out.println(2|3);
    }
}
